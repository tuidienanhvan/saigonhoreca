// Self-healing: Tự động kiểm tra, khôi phục hoặc nâng cấp các file cấu hình hệ thống (.htaccess, robots.txt, og-default.png) ở WordPress root
add_action('wp_loaded', function() {
    $wp_root = ABSPATH;
    $htaccess_path = $wp_root . '.htaccess';
    $robots_path   = $wp_root . 'robots.txt';
    $og_image_path = $wp_root . 'og-default.png';

    $need_htaccess = !file_exists($htaccess_path);
    if (!$need_htaccess) {
        // Nếu .htaccess đã tồn tại, kiểm tra xem đã có cấu hình tối ưu của theme (SGH_STATIC_CACHE) chưa
        $htaccess_content = @file_get_contents($htaccess_path);
        if ($htaccess_content !== false && strpos($htaccess_content, 'SGH_STATIC_CACHE') === false) {
            $need_htaccess = true; // Cần đè/nâng cấp lên bản premium của theme
        }
    }

    $need_robots = !file_exists($robots_path);
    if (!$need_robots) {
        // Nếu robots.txt đã tồn tại, kiểm tra xem đã có cấu hình chuẩn SEO của theme chưa
        $robots_content = @file_get_contents($robots_path);
        if ($robots_content !== false && strpos($robots_content, 'Generated by Saigon Horeca Theme') === false) {
            $need_robots = true; // Cần đè/nâng cấp bản tối ưu mới nhất
        }
    }

    $need_og_image = !file_exists($og_image_path);

    if ($need_htaccess || $need_robots || $need_og_image) {
        $deploy_file = get_template_directory() . '/inc/admin/sync/sync-deploy.php';
        if (file_exists($deploy_file)) {
            require_once $deploy_file;
        }

        // Tự động deploy / khôi phục các file cấu hình tương ứng
        if ($need_htaccess && function_exists('sgh_dashboard_deploy_htaccess')) {
            sgh_dashboard_deploy_htaccess();
        }
        if ($need_robots && function_exists('sgh_dashboard_deploy_robots_txt')) {
            sgh_dashboard_deploy_robots_txt();
        }
        if ($need_og_image && function_exists('sgh_dashboard_deploy_og_image')) {
            sgh_dashboard_deploy_og_image();
        }
    }

    // Auto-sync theme file templates' "* Thumbnail:" values directly into WP database (_thumbnail_id)
    if (is_admin() || !wp_doing_ajax()) {
        try {
            $projects = get_posts([
                'post_type'      => 'project',
                'post_status'    => 'any',
                'posts_per_page' => -1,
                'no_found_rows'  => true,
            ]);
            
            if (!empty($projects)) {
                foreach ($projects as $proj) {
                    $slug = $proj->post_name;
                    if (empty($slug)) continue;

                    if (function_exists('sgh_get_project_thumbnail')) {
                        $custom_thumb = sgh_get_project_thumbnail($slug);
                        if (!empty($custom_thumb)) {
                            // We have a custom template thumbnail defined. 
                            // Check if _thumbnail_id is set or matches this media attachment.
                            $existing_thumb_id = get_post_thumbnail_id($proj->ID);
                            
                            // Parse relative file path from thumbnail URL
                            $uploads_url = function_exists('sgh_uploads_base_url') ? sgh_uploads_base_url() : wp_get_upload_dir()['baseurl'];
                            $rel_path = str_replace($uploads_url . '/', '', $custom_thumb);
                            
                            if (empty($existing_thumb_id)) {
                                // Find or insert the attachment in the DB
                                $attach_id = attachment_url_to_postid($custom_thumb);
                                if (!$attach_id) {
                                    $uploads_dir = function_exists('sgh_uploads_base_dir') ? sgh_uploads_base_dir() : wp_get_upload_dir()['basedir'];
                                    $file_path = wp_normalize_path($uploads_dir . '/' . $rel_path);
                                    
                                    if (file_exists($file_path)) {
                                        $wp_filetype = wp_check_filetype($file_path, null);
                                        $attachment = [
                                            'post_mime_type' => $wp_filetype['type'],
                                            'post_title'     => sanitize_file_name(basename($file_path)),
                                            'post_content'   => '',
                                            'post_status'    => 'inherit'
                                        ];
                                        $attach_id = wp_insert_attachment($attachment, $file_path, $proj->ID);
                                        if (!is_wp_error($attach_id) && $attach_id > 0) {
                                            require_once ABSPATH . 'wp-admin/includes/image.php';
                                            $attach_data = wp_generate_attachment_metadata($attach_id, $file_path);
                                            wp_update_attachment_metadata($attach_id, $attach_data);
                                        }
                                    }
                                }
                                
                                if ($attach_id && !is_wp_error($attach_id)) {
                                    set_post_thumbnail($proj->ID, $attach_id);
                                }
                            }
                        }
                    }
                }
            }
        } catch (\Throwable $ex) {
            // Safe fallback to prevent active process crash
        }
    }
});

