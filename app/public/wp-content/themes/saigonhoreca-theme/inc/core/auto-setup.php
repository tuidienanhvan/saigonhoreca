/**
 * ─────────────────────────────────────────────────────────────────────
 * VIETNAMESE UNICODE NORMALIZER (NFC COMPOSER)
 *
 * Tự động kiểm tra và chuẩn hóa mọi chuỗi tiếng Việt từ dạng tổ hợp (NFD)
 * sang dạng dựng sẵn (NFC). Giải quyết triệt để lỗi font chữ bị rách dấu,
 * rải rác dấu (ví dụ să ́p → sắp) trên toàn trang WordPress.
 * ─────────────────────────────────────────────────────────────────────
 */
if (!function_exists('sgh_normalize_vietnamese_nfc')) {
    function sgh_normalize_vietnamese_nfc($text) {
        if (empty($text) || !is_string($text)) {
            return $text;
        }

        // Chỉ xử lý nếu class Normalizer (intl extension) tồn tại
        if (class_exists('Normalizer')) {
            if (Normalizer::isNormalized($text, Normalizer::FORM_C) === false) {
                return Normalizer::normalize($text, Normalizer::FORM_C);
            }
        }

        return $text;
    }
}

// Hook vào gettext lọc dịch PO/MO lỗi dấu tổ hợp từ server production crawl về
add_filter('gettext', 'sgh_normalize_vietnamese_nfc', 9999);
add_filter('gettext_with_context', 'sgh_normalize_vietnamese_nfc', 9999);

// Hook vào các filter nội dung chính để chuẩn hóa dữ liệu đầu ra từ database hoặc file PHP
add_filter('the_content', 'sgh_normalize_vietnamese_nfc', 9999);
add_filter('the_title', 'sgh_normalize_vietnamese_nfc', 9999);
add_filter('the_excerpt', 'sgh_normalize_vietnamese_nfc', 9999);
add_filter('widget_text', 'sgh_normalize_vietnamese_nfc', 9999);
add_filter('widget_title', 'sgh_normalize_vietnamese_nfc', 9999);

/**
 * Tự động gỡ bỏ các hàm dịch gettext lỗi tiếng Việt trong thư mục project-pillar/amdang-typhoon
 * và thay thế bằng in trực tiếp chuẩn NFC, tránh đè bản dịch lỗi NFD từ file .mo/.po.
 * Trigger bằng cách truy cập: https://saigonhoreca.local/du-an/amdang-typhoon/?sgh_strip_gettext=1
 */
add_action('wp_loaded', function() {
    if (isset($_GET['sgh_strip_gettext'])) {
        $dir = get_template_directory() . '/template-parts/project-pillar/amdang-typhoon';
        if (is_dir($dir)) {
            $files = glob($dir . '/*.php');
            $count = 0;
            foreach ($files as $file) {
                $content = @file_get_contents($file);
                if ($content !== false) {
                    // 1. Thay thế esc_html__('text', 'saigonhoreca') bằng esc_html('text')
                    $pattern_html = "/esc_html__\(\s*'((?:[^'\\\\]|\\\\.)*)'\s*,\s*'saigonhoreca'\s*\)/u";
                    $content = preg_replace($pattern_html, "esc_html('$1')", $content);

                    // 2. Thay thế esc_attr__('text', 'saigonhoreca') bằng esc_attr('text')
                    $pattern_attr = "/esc_attr__\(\s*'((?:[^'\\\\]|\\\\.)*)'\s*,\s*'saigonhoreca'\s*\)/u";
                    $content = preg_replace($pattern_attr, "esc_attr('$1')", $content);

                    // 3. Thay thế __('text', 'saigonhoreca') bằng esc_html('text')
                    $pattern_double = "/__\(\s*'((?:[^'\\\\]|\\\\.)*)'\s*,\s*'saigonhoreca'\s*\)/u";
                    $content = preg_replace($pattern_double, "esc_html('$1')", $content);

                    @file_put_contents($file, $content);
                    $count++;
                }
            }
            wp_die('Gettext strip completed. Cleaned ' . $count . ' files.');
        }
        wp_die('Amdang Typhoon directory not found.');
    }
});



